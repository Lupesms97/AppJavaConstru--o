package Metodos;

public class BackEndCaluladora {


    public void somar(int var1, int var2){
        int resultado = var1 + var2;
        System.out.println("O valor da soma dos numeros digitados é:" + resultado);
    }

    public void diminuir(int var1, int var2){
        int resultado = var1 - var2;
        System.out.println("O valor da subtração dos numeros digitados é:" + resultado);
    }

    public void dividir(int var1, int var2){
        int resultado = var1 / var2;
        System.out.println("O valor da divisão dos numeros digitados é:" + resultado);
    }

    public void multiplicar(int var1, int var2){
        int resultado = var1 * var2;
        System.out.println("O valor da multiplicação ds números digitados é:" + resultado);
    }
    



}
